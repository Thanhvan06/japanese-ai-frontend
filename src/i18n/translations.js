// Helper function to get translation with replacements
export const t = (key, language = "vi", replacements = {}) => {
  const keys = key.split(".");
  let value = translations[language];

  for (const k of keys) {
    if (value && typeof value === "object") {
      value = value[k];
    } else {
      return key; // Return key if translation not found
    }
  }

  if (typeof value !== "string") {
    return key;
  }

  // Replace placeholders like {count}, {query}, etc.
  return value.replace(/\{(\w+)\}/g, (match, placeholder) => {
    return replacements[placeholder] !== undefined ? replacements[placeholder] : match;
  });
};
